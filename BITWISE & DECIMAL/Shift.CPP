#include<iostream>
using namespace std;
int main(){
    int a=5;
    int b=7;
    cout<<" a&b "<< (a&b) <<endl;
    cout<<" a|b "<< (a|b) <<endl;
    cout<<" ~a "<< (~a) <<endl;
    cout<<" a^b "<< (a^b) <<endl;

    cout<< (17>>1) <<endl;  //right shift
    cout<< (17>>2) <<endl;
    cout<< (19<<1) <<endl;  //left shift
    cout<< (19<<2) <<endl;
} 